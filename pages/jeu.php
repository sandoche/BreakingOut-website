<p>BreakingOut, est un PHPRPG où vous incarnez un prisonnier de Saint Hilton Jail.<br /><br />
Vous avez pour but d'essayer de survir de cette univers infame, en vous battant, et en vivant votre vie tout en tentant de vous evader.
</p>

<div align="justify"><strong>Description du jeu :</strong><br />
  Le personnage pr&eacute;sente 4 caract&eacute;ristiques qui d&eacute;finissent le niveau<span class="Style1A"> (Force, Agilit&eacute;, Intelligence, Respect).</span><br />
  Ainsi que 4 caract&eacute;ristiques variables et tr&egrave;s influentes <span class="Style1A">(Sant&eacute;, Hygi&egrave;ne, Moral, Faim)</span><br />
  que vous devrez maintenir a un certain niveau pour ne pas sombrer dans la d&eacute;prime, la famine, la &quot;puanteur&quot; ou m&ecirc;me la mort <img src="http://www.siteduzero.com/Templates/images/smilies/rire.gif" alt=":lol:" /> <br />
  <br />
  A vous de g&eacute;rer votre personnage dans cette prison avec ses lieux incontournable :<br />
  <span class="Style1A">-L'infirmerie</span> (pour vous soigner)<br />
  <span class="Style1A">-La cantine</span> (pour manger)<br />
  <span class="Style1A">-L'industrie p&eacute;nitenci&egrave;re</span> (pour travailler, evoluer ses caract&eacute;ristiques)<br />
  <span class="Style1A">-La salle du gang</span> (pour s'&eacute;vader)<br />
  <span class="Style1A">-La cellule</span> (pour s'entrainer, voir ses  cararct&eacute;ristiques et statistiques, am&eacute;liorer son intelligence ou son  moral en demandant rencontres avec sa famille).<br />
  <span class="Style1A">-La cour</span> (un chat, et un lieu ou la  majorit&eacute; des actions se d&eacute;roulent : d&eacute;fies au combat, au &quot;poker&quot;, c'est  la ou vous imposerez la puissance de votre gang, la ou vous vous ferez  des amis, des ennemis, la ou vous ferez du sport, ...)<br />
  <br />
  <br />
  <strong>Calendrier du jeu et gestion des points :</strong><br />
  Le jeu fonctionne grace &agrave; des <span class="Style1A">PA (Points d'Actions)</span> pour la majorit&eacute; des actions. Ceux ci sont disponibles tout les jours de prison (toutes les 6 heures dans la vraie vie), on a donc <span class="Style1A">20 PA, 4 fois par jour </span>au maximum.<br />
Par ailleur les actions dans la cours s'effectuent avec des<span class="Style1A"> PM (Points de Map)</span>, ceux ci sont accumulable mais pas offert (sauf &agrave; l'inscription 100 PM sont offerts) et<span class="Style1A"> 1 PA = 10 PM. </span><br />
Les PM permettent de faire du sport dans la cour, prendre des douches, d&eacute;fier quelqu'un au poker ou au combat.<br />
Les PA permettent tout le reste.<br />
<br />
<br />
<strong>R&eacute;compense <img src="http://www.siteduzero.com/Templates/images/smilies/heureux.png" alt=":D" /> </strong> :<br />
<span class="Style2A">Le premier &eacute;vad&eacute; de Saint Hilton Jail (la prison de Breaking Out) empoche 20&euro;</span> (au minimum cette somme pourrait augmenter).<br />
Pour s'&eacute;vader il suffit de r&eacute;unir un gang de 8, de creuser, d'avoir des  bonnes relations avec l'infirmi&egrave;re (comme la fait un certain Michael  Scofield <img src="http://www.siteduzero.com/Templates/images/smilies/siffle.png" alt=":-&deg;" /> ) et d'avoir son aide externe &agrave; la prison (en envoyant le lien d'aide &agrave; ses amis <img src="http://www.siteduzero.com/Templates/images/smilies/hihi.png" alt="^^" /> ).<br />
