BreakingOut est un jeu gratuit.<br />
Le jeu étant en béta les règles peuvent êtres modifés.
<br />
<br />
<b>Comment jouer ?</b>
<br />
Chaque jour de prison (1 jour = 6 h dans la réalité) vous gagnez 20 PA (Points d'Actions) avec lesquels vous pouvez faire des actions tel que Travailler, se battre, faire du sport, jouer au poker, s'évader.
<br />
Chaque action a un coût en PA.
<br />
<br />
<b>Chaque joueur à le droit de jouer qu'un seul prisonnier : le multi-compte est interdit.</b>
<br /><br />
Tout joueur qui effectue des actions que l'administrateur considère comme illégale (tricherie, exploitation d'un bug, abus, ...) pourra être sanctionné.
<br /><br />
Pour plus d'informations veuillez consulter la <a href='http://breakingout.free.fr/forum/index.php?page=post&ids=229' target='_blank'>F.A.Q.</a>
